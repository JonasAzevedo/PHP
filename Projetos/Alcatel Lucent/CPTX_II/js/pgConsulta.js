function abrirPagina(url) {

  var width = 200;
  var height = 200;
  var left = 400;
  var top = 200;

  window.open(url,'janela', 'width='+width+', height='+height+', top='+top+', left='+left+', scrollbars=yes, status=no, toolbar=no, location=no, directories=no, menubar=no, resizable=no, fullscreen=no');
}

